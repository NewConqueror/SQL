-- DATEADD
DECLARE @TARIH AS DATETIME
SET @TARIH='2023-01-01 00:00:00'
DECLARE @TARIH2 AS DATETIME
SET @TARIH2=DATEADD(YEAR,6,@TARIH)
SET @TARIH2=DATEADD(MONTH,6,@TARIH)
SET @TARIH2=DATEADD(DAY,6,@TARIH)
SET @TARIH2=DATEADD(HOUR,6,@TARIH)
SET @TARIH2=DATEADD(MINUTE,6,@TARIH)
SET @TARIH2=DATEADD(SECOND,6,@TARIH)

SELECT @TARIH,@TARIH2

-- DATEDIFF
DECLARE @TARIH3 AS DATETIME
	SET @TARIH3='2023-01-01 00:00:00'
DECLARE @TARIH4 AS DATETIME
	SET @TARIH4='2022-01-01 00:00:00'

SELECT DATEDIFF(YEAR,@TARIH3,@TARIH4)
SELECT DATEDIFF(MONTH,@TARIH3,@TARIH4)
SELECT DATEDIFF(DAY,@TARIH3,@TARIH4)
SELECT DATEDIFF(MINUTE,@TARIH3,@TARIH4)
SELECT DATEDIFF(SECOND,@TARIH3,@TARIH4)
SELECT DATEDIFF(WEEK,@TARIH3,@TARIH4)

